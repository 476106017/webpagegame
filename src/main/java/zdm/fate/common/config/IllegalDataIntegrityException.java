package zdm.fate.common.config;

public class IllegalDataIntegrityException extends Exception {
	/**
	 * 
	 */
	private static final long serialVersionUID = -1176795894335459661L;

	public IllegalDataIntegrityException() {
		super();
	}

	public IllegalDataIntegrityException(Throwable cause) {
		super(cause);
	}

	public IllegalDataIntegrityException(String string) {
		super(string);
	}

}
